package com.example.gongjiao.pojo;

public class siji {

	String id;
	String name;
	String piaoshu;
	public siji() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPiaoshu() {
		return piaoshu;
	}
	public void setPiaoshu(String piaoshu) {
		this.piaoshu = piaoshu;
	}
}
